# 使用圆括号创建元组
tup1 = (1, 2, 3)

# 不使用圆括号，但使用逗号分隔
tup2 = 1, 2, 3

# 只有一个元素的元组需要在元素后面加逗号
tup3 = (1,)

print(tup1)  # 输出: (1, 2, 3)
print(tup2)  # 输出: (1, 2, 3)
print(tup3)  # 输出: (1,)
tup = (1, 2, 3, 4, 5)
print(tup[0])  # 输出: 1
print(tup[-1])  # 输出: 5，负索引表示从末尾开始计数
tup = (1, 2, 3, 4, 5)
print(tup[1:3])  # 输出: (2, 3)
tup = (1, 2, 3)
# tup[0] = 4  # 这会引发TypeError
tup = (1, [2, 3], 4)  
tup[1].append(5)  # 修改元组中列表的元素  
print(tup)  # 输出: (1, [2, 3, 5], 4)